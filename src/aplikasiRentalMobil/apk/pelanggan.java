    private void tablePelangganMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablePelangganMouseClicked
        // TODO add your handling code here:
        int baris = tablePelanggan.rowAtPoint(evt.getPoint());
        String kode = tablePelanggan.getValueAt(baris, 1).toString();
        tfKode.setText(kode);
        String nama = tablePelanggan.getValueAt(baris, 2).toString();
        tfNama.setText(nama);
        String alamat = tablePelanggan.getValueAt(baris, 3).toString();
        tfAlamat.setText(alamat);
        String no_telepon = tablePelanggan.getValueAt(baris, 4).toString();
        tfNoTelp.setText(no_telepon);        
               
        tfKode.setEnabled(true);
        tfNama.setEnabled(true);
        tfAlamat.setEnabled(true);
        tfNoTelp.setEnabled(true);
        btnSimpan.setEnabled(false);
        btnUbah.setEnabled(true);
        btnTambah.setEnabled(false);
        btnBatal.setEnabled(true); 
        btnHapus.setEnabled(true);
    }//GEN-LAST:event_tablePelangganMouseClicked

