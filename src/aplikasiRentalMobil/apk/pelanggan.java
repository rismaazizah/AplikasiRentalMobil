    private void TampilData (){
        DftTbModel_pelanggan = new DefaultTableModel();
        DftTbModel_pelanggan.addColumn("Nomor");
        DftTbModel_pelanggan.addColumn("Kode Pelanggan");
        DftTbModel_pelanggan.addColumn("Nama Pelanggan");
        DftTbModel_pelanggan.addColumn("Alamat");
        DftTbModel_pelanggan.addColumn("No Telepon");
        
        tablePelanggan.setModel(DftTbModel_pelanggan);
        Connection conn = conek.getConnection();
        String cari = tfCari.getText();
        try{
            int no=1;
            java.sql.Statement stmt = conn.createStatement();
            SQL = "select * from tb_pelanggan where nama_pelanggan LIKE '%"+cari+"%'";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while (res.next()) {
                DftTbModel_pelanggan.addRow(new Object[]{
                    no++,
                    res.getString("id_pelanggan"),
                    res.getString("nama_pelanggan"),
                    res.getString("alamat"),
                    res.getString("no_telepon"), });               
                }
            } catch (SQLException e){
            System.out.println(e.getMessage());
            }       
    }
    
