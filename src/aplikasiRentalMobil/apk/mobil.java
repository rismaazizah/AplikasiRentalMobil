    private void TampilData (){
        DftTbModel_mobil = new DefaultTableModel();
        DftTbModel_mobil.addColumn("Nomor");
        DftTbModel_mobil.addColumn("Kode Mobil");
        DftTbModel_mobil.addColumn("Merk Mobil");
        DftTbModel_mobil.addColumn("Tipe Mobil");
        DftTbModel_mobil.addColumn("Warna");
        DftTbModel_mobil.addColumn("Tahun Keluaran");
        DftTbModel_mobil.addColumn("Harga Sewa");
        DftTbModel_mobil.addColumn("Status");
        
        tabelMobil.setModel(DftTbModel_mobil);
        Connection conn = conek.getConnection();
        String cari = tfCari.getText();
        try{
            int no=1;
            java.sql.Statement stmt = conn.createStatement();
            SQL = "select * from tb_mobil where merk_mobil LIKE '%"+cari+"%'";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while (res.next()) {
                DftTbModel_mobil.addRow(new Object[]{
                    no++,
                    res.getString("id_mobil"),
                    res.getString("merk_mobil"),
                    res.getString("tipe_mobil"),
                    res.getString("warna"),
                    res.getString("tahun_keluaran"),
                    res.getString("harga_sewa"),
                    res.getString("status"), });               
                }
            } catch (SQLException e){
            System.out.println(e.getMessage());
            }
    }     

    
