    private void tabelMobilMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMobilMouseClicked
        // TODO add your handling code here:        
        int baris = tabelMobil.rowAtPoint(evt.getPoint());
        String kode = tabelMobil.getValueAt(baris, 1).toString();
        tfKode.setText(kode);
        String merk = tabelMobil.getValueAt(baris, 2).toString();
        tfMerk.setText(merk);
        String tipe = tabelMobil.getValueAt(baris, 3).toString();
        tfTipe.setText(tipe);
        String warna = tabelMobil.getValueAt(baris, 4).toString();
        tfWarna.setText(warna);
        String tahun = tabelMobil.getValueAt(baris, 5).toString();
        tfTahun.setText(tahun);
        String harga = tabelMobil.getValueAt(baris, 6).toString();
        tfHarga.setText(harga);
        String status = tabelMobil.getValueAt(baris, 7).toString();
        cbStatus.setSelectedItem(status);
        
        tfKode.setEnabled(true);
        tfMerk.setEnabled(true);
        tfTipe.setEnabled(true);
        tfWarna.setEnabled(true);
        tfTahun.setEnabled(true);
        tfHarga.setEnabled(true);
        cbStatus.setEnabled(true);
        btnSimpan.setEnabled(false);
        btnUbah.setEnabled(true);
        btnTambah.setEnabled(false);
        btnBatal.setEnabled(true); 
        btnHapus.setEnabled(true);
    }//GEN-LAST:event_tabelMobilMouseClicked

